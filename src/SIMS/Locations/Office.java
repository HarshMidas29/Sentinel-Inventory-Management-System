    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:                               
        String name = txtName.getText(); // Change variable name from txtMobileNumber to txtName
        String address = txtAddress.getText(); // Change variable name from txtEmail to txtAddress

        if (validateFields("new")) {
            JOptionPane.showMessageDialog(null, "Please enter all fields");
        } else {
            try {
                Connection con = ConnectionProvider.getCon();
                PreparedStatement ps = con.prepareStatement("insert into office (name, address) values (?,?)"); // Update table and column names
                ps.setString(1, name);
                ps.setString(2, address);
                ps.executeUpdate();
                JOptionPane.showMessageDialog(null, "Office Added Successfully"); // Update message
                setVisible(false);
                new Office().setVisible(true);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }

    }//GEN-LAST:event_btnSaveActionPerformed

    private void OfficesTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_OfficesTableMouseClicked
        // TODO add your handling code here:                                  
        // TODO add your handling code here:
        int index = OfficesTable.getSelectedRow();
        TableModel model = OfficesTable.getModel();

        String id = model.getValueAt(index, 0).toString();
        locationPk = Integer.parseInt(id); // Change variable name from customerPk to locationPk
        String name = model.getValueAt(index, 1).toString(); // Change variable name from mobileNumber to name
        txtName.setText(name); // Change variable name from txtMobileNumber to txtName
        String address = model.getValueAt(index, 2).toString(); // Change variable name from email to address
        txtAddress.setText(address); // Change variable name from txtEmail to txtAddress

        btnSave.setEnabled(false);
        btnUpdate.setEnabled(true);
    }//GEN-LAST:event_OfficesTableMouseClicked

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:                                       
        // TODO add your handling code here:
        String name = txtName.getText(); // Change variable name from txtMobileNumber to txtName
        String address = txtAddress.getText(); // Change variable name from txtEmail to txtAddress

        if (validateFields("edit")) {
            JOptionPane.showMessageDialog(null, "Please enter all fields");
        } else {
            try {
                Connection con = ConnectionProvider.getCon();
                PreparedStatement ps = con.prepareStatement("update office set name=?, address=? where id=?"); // Update table and column names

                ps.setString(1, name);
                ps.setString(2, address);
                ps.setInt(3, locationPk);
                ps.executeUpdate();
                JOptionPane.showMessageDialog(null, "Office Updated Successfully"); // Update message
                setVisible(false);
                new Office().setVisible(true);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }

    }//GEN-LAST:event_btnUpdateActionPerformed

